#include <iostream>
using namespace std;

int main() {
    int a = 5;
    int b = 10;

    int sum = a + b;
    int difference = a - b;
    int multiply = a * b;
    double divide = (double) a / b;

    cout << "sum " << sum << endl;
    cout << "difference " << difference << endl;
    cout << "multiply " << multiply << endl;
    cout << "divide " << divide << endl;

    return 0;

}